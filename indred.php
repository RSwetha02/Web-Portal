<h1><center>INVERT SYRUP</center> </h1>
<h3><b>GLUCOSE SYRUP</b></h3>
<p> Glucose syrup (C6H12O6) is a solution (up to 80%) of glucose (dextrose), maltose and maltodextrins in water. It is normally obtained by enzymatic hydrolysis of starch. Starch from wheat, corn, potato, cassava or any other plant can be used for this purpose.
Standard glucose syrup has a DE value (dextrose equivalent) of 42. The relative sweetness of 42DE glucose to sucrose is 40-45%.</p>
<h3><b>CANE SYRUP 80%</b></h3>
<p>Syrups with 80% solids derived from the refining of cane sugar. Used for their excellent flavour. </p>
<h3><b>INVERT SYRUP 70%</b></h3>
<p>Syrup of 70% solids made by acid hydrolysis of sucrose.
The result is a 50:50 mixture of dextrose and fructose which are both reducing sugars and contribute to the Maillard reaction in baking.</p>
<h3><b>FRUCTOSE SYRUP 80%</b></h3>
<p> Commercially, fructose is usually derived from sugar cane, sugar beets and corn.
Crystalline fructose is a monosaccharide, dried and ground, and of high purity. High-fructose corn syrup (HFCS) is a mixture of glucose and fructose. 
High fructose syrup is a special type of glucose, formulated with a high fructose content. It is much sweeter than glucose.</p>
<h3><b>MALT EXTRACT 80%</b></h3>
<p>A thick glutinous syrup of 80% solids usually non-diastatic and obtained by water extraction of malted wheat or barley. 
The heat treatment used to concentrate the solution destroys any enzymes. Used as one of the important flavour ingredients for biscuits.. Rich in maltose, which is a reducing sugar."</p>
